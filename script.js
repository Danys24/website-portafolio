((d) => {
  const $form = d.querySelector(".form-contact"),
    $response = d.querySelector(".input-btn");

  $form.addEventListener("submit", (e) => {
    e.preventDefault();
    fetch("https://formsubmit.co/ajax/danisserna99@gmail.com", {
      method: "POST",
      body: new FormData(e.target),
    })
      .then((res) => (res.ok ? res.json() : Promise.reject(res)))
      .then((json) => {
        console.log(json);
        $form.reset();
      })
      .catch((err) => {
        console.log(err);
        let message =
          err.statusText || "OcurriÃ³ un error al enviar, intenta nuevamente";
        $response.querySelector(
          "h3"
        ).innerHTML = `Error ${err.status}: ${message}`;
      })
      .finally(() => {
      });
  });
})(document);